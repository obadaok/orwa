    private val categoriesOrder = listOf(
        "أدعية النبي ﷺ", "أدعية الصباح", "أدعية المساء", "أدعية النوم",
        "أدعية الكرب", "أدعية السفر", "أدعية الرزق", "أدعية الاستغفار",
        "أدعية القرآن", "أدعية الأنبياء", "أدعية عامة"
    )

    fun getAllCategories(): List<String> = categoriesOrder

    fun getDuasByCategory(category: String): List<DuaItem> = allDuas.filter { it.category == category }

    fun getGroupedDuas(): List<DuaGroupedItem> {
        val result = mutableListOf<DuaGroupedItem>()
        for (category in categoriesOrder) {
            val duas = allDuas.filter { it.category == category }
            if (duas.isNotEmpty()) {
                result.add(DuaGroupedItem.Header(category))
                result.addAll(duas.map { DuaGroupedItem.Item(it) })
            }
        }
        return result
    }

    fun search(query: String): List<DuaGroupedItem> {
        if (query.isBlank()) return getGroupedDuas()
        val q = query.trim()

        val results = allDuas.filter {
            it.arabicText.contains(q) ||
            it.title.contains(q) ||
            it.category.contains(q) ||
            it.translation.contains(q) ||
            it.source.contains(q)
        }

        if (results.isEmpty()) return emptyList()

        val grouped = mutableListOf<DuaGroupedItem>()
        val seenCategories = mutableSetOf<String>()
        for (dua in results) {
            if (dua.category !in seenCategories) {
                seenCategories.add(dua.category)
                grouped.add(DuaGroupedItem.Header(dua.category))
            }
            grouped.add(DuaGroupedItem.Item(dua))
        }
        return grouped
    }
