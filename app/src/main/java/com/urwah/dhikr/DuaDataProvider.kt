    fun getGroupedDuas(): List<DuaGroupedItem> {
        val result = mutableListOf<DuaGroupedItem>()
        var currentCategory = ""
        for (dua in allDuas) {
            if (dua.category != currentCategory) {
                currentCategory = dua.category
                result.add(DuaGroupedItem.Header(currentCategory))
            }
            result.add(DuaGroupedItem.Item(dua))
        }
        return result
    }

    fun searchDuas(query: String): List<DuaGroupedItem> {
        if (query.isBlank()) return getGroupedDuas()
        val q = query.trim().lowercase()
        val matching = allDuas.filter {
            it.arabicText.contains(q, ignoreCase = true) ||
            it.translation.contains(q, ignoreCase = true) ||
            it.category.contains(q, ignoreCase = true) ||
            it.title.contains(q, ignoreCase = true)
        }
        val result = mutableListOf<DuaGroupedItem>()
        var currentCategory = ""
        for (dua in matching) {
            if (dua.category != currentCategory) {
                currentCategory = dua.category
                result.add(DuaGroupedItem.Header(currentCategory))
            }
            result.add(DuaGroupedItem.Item(dua))
        }
        return result
    }
